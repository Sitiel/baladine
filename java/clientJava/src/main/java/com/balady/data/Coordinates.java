package com.balady.data;

public class Coordinates {
	private float x;
	private float y;
	/**
	 * @param x
	 * @param y
	 */
	public Coordinates(float x, float y) {
		this.x = x;
		this.y = y;
	}
	/**
	 * @return the x
	 */
	public float getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(float x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public float getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(float y) {
		this.y = y;
	}
	
	
}
